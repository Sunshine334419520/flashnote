/** Group notes by time period */
export type TimeGroup = 'Today' | 'Yesterday' | 'This Week' | 'Earlier'

export function groupNotesByTime(notes: Note[]): { group: TimeGroup; notes: Note[] }[] {
  const now2 = new Date()
  const startOfToday = new Date(now2.getFullYear(), now2.getMonth(), now2.getDate())
  const startOfYesterday = new Date(startOfToday.getTime() - 86400000)
  const startOfWeek = new Date(startOfToday.getTime() - startOfToday.getDay() * 86400000)

  const groups: Record<TimeGroup, Note[]> = {
    Today: [],
    Yesterday: [],
    'This Week': [],
    Earlier: [],
  }

  for (const note of notes) {
    const d = new Date(note.updatedAt)
    if (d >= startOfToday) {
      groups['Today'].push(note)
    } else if (d >= startOfYesterday) {
      groups['Yesterday'].push(note)
    } else if (d >= startOfWeek) {
      groups['This Week'].push(note)
    } else {
      groups['Earlier'].push(note)
    }
  }

  return Object.entries(groups)
    .filter(([, notes]) => notes.length > 0)
    .map(([group, notes]) => ({ group: group as TimeGroup, notes }))
}

